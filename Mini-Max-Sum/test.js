const miniMaxSum = require("./index.js");

test("Should exist", () => {
  expect(miniMaxSum).not.toBe(undefined);
});

test("Should return a string of numbers separated by space", () => {
  const res = miniMaxSum([0, 1, 36, 88, 38]);
  expect(typeof res).toBe("string");
  expect(res.includes(" ")).toBe(true);
  const [a, b] = res.split(" ");
  expect(+a).toBe(75);
  expect(+b).toBe(163);
})

test("Should return the valid sums", () => {
  expect(miniMaxSum([1, 2, 3, 4, 5, 6, 7, 8, 9])).toBe("10 30");
});

